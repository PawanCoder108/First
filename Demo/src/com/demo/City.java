package com.demo;

public class City {
	public String name;

	public City() {}
	/**
	 * @param name
	 */
	public City(String name) {
		super();
		this.name = name;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
	
	

}
